const PERMISSIONS = {
  // Personal & Usuarios
  MANAGE_STAFF: 'MANAGE_STAFF',
  VIEW_STAFF: 'VIEW_STAFF',

  // Producción
  MANAGE_PRODUCTION: 'MANAGE_PRODUCTION',
  VIEW_PRODUCTION: 'VIEW_PRODUCTION',
  ASSIGN_OPERATIONS: 'ASSIGN_OPERATIONS',

  // Calidad
  MANAGE_QUALITY: 'MANAGE_QUALITY',
  VIEW_QUALITY: 'VIEW_QUALITY',

  // Máquinas
  VIEW_MACHINES: 'VIEW_MACHINES',
  MANAGE_MACHINES: 'MANAGE_MACHINES',

  // Sistema
  VIEW_AUDIT: 'VIEW_AUDIT',
  VIEW_PROCESSES: 'VIEW_PROCESSES'
};

const ROLE_PERMISSIONS = {
  'Administrador': Object.values(PERMISSIONS),
  'Inspector': [
    PERMISSIONS.VIEW_STAFF,
    PERMISSIONS.VIEW_PRODUCTION,
    PERMISSIONS.MANAGE_PRODUCTION,
    PERMISSIONS.VIEW_QUALITY,
    PERMISSIONS.MANAGE_QUALITY,
    PERMISSIONS.VIEW_MACHINES,
    PERMISSIONS.MANAGE_MACHINES,
    PERMISSIONS.VIEW_PROCESSES,
    PERMISSIONS.VIEW_AUDIT
  ],
  'Supervisor': [
    PERMISSIONS.VIEW_STAFF,
    PERMISSIONS.VIEW_PRODUCTION,
    PERMISSIONS.ASSIGN_OPERATIONS,
    PERMISSIONS.VIEW_QUALITY,
    PERMISSIONS.VIEW_MACHINES,
    PERMISSIONS.VIEW_PROCESSES
  ],
  'Jefe de Operaciones': [
    PERMISSIONS.VIEW_STAFF,
    PERMISSIONS.MANAGE_STAFF,
    PERMISSIONS.VIEW_PRODUCTION,
    PERMISSIONS.VIEW_QUALITY,
    PERMISSIONS.VIEW_MACHINES,
    PERMISSIONS.MANAGE_MACHINES,
    PERMISSIONS.VIEW_PROCESSES
  ],
  'Gerencia': [
    PERMISSIONS.VIEW_STAFF,
    PERMISSIONS.VIEW_PRODUCTION,
    PERMISSIONS.VIEW_QUALITY,
    PERMISSIONS.VIEW_MACHINES,
    PERMISSIONS.VIEW_AUDIT,
    PERMISSIONS.VIEW_PROCESSES
  ],
  'Operario': [
    PERMISSIONS.VIEW_PRODUCTION,
    PERMISSIONS.MANAGE_PRODUCTION
  ]
};

const hasPermission = (role, permission) => {
  if (!role || !permission) return false;
  const permissions = ROLE_PERMISSIONS[role] || [];
  return permissions.includes(permission);
};

module.exports = {
  PERMISSIONS,
  ROLE_PERMISSIONS,
  hasPermission
};
